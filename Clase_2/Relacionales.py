#Relacionales

a=10
b=5

#Igual a 

resultado_igual=(a==b)

print(f'{a} es igual a {b} ? {resultado_igual} ')

#Resultado no igual

resultado_noigual= (a!=b)

print(f'{a} no es igual a {b} ? {resultado_noigual} ')

#Mayor que

resultado_mayor= (a > b)
print(f'{a} es mayor que {b} ? {resultado_mayor}')    

#Menor que

resultado_menor= (a < b)

print(f'{a} es menor que {b} ? {resultado_menor}')    

#Mayor o igual que 
mayor_igual= (a>=b)

print(f'{a} es mayor o igual que {b}? {mayor_igual}')

# Menor o igual que
menor_igual= (a<=b)

print(f'{a} es menor o igual que {b}? {menor_igual}')



