#Ejemplos 
# Asignación simple

x=10

print(f'X={x}')

#Suma y asignación

x+=5

print(f'X+={x}')

#Resta y asignación

x-=3

print(f'X-={x}')

#Multiplicación y asignación

x*=4 #Equivale a x * 4

print(f'X*={x}')

#División y asignación

x/=2

print(f'X/={x}')

#División entera y asignación

x//=5

print(f'X//={x}')

#Módulo y asignación

x%=8

print(f'X%={x}')

#Potencializar y asignación 

x**= 2
print(f'X**={x}')







