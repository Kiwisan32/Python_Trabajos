#Modificar y Eliminar Elementos de un Diccionario
#1. Usando el diccionario del ejercicio anterior, actualiza el año de
#publicación a 1968.
#2. Elimina el género del diccionario.
#3. Imprime el diccionario después de cada operación.
#

detalles_libro= {

     'Título':'Norte y Sur',
     'Autor':'Elizabeth Gaskell',
     'Año de publicación':1855,
     'Género': 'Novela Social'
}

print('Detalles del libro:', detalles_libro)

#Actualizar el año de publicación

detalles_libro['Año de publicación']=1968
print('\nActualización del año de publicación del libro:', detalles_libro)

#Elimina el género del diccionario

del detalles_libro ['Género']
print('\nActualización de la información del libro:', detalles_libro, '\n')
